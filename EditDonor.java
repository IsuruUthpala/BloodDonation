    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet rs;
            Statement stmt = new DAC().getConnection().createStatement();
            String query = "SELECT * FROM  donor WHERE DonorID = '" + jTextField1.getText() + "' ";
            // Select * from table Where 
            rs = stmt.executeQuery(query);

            while (rs.next()) {
                jTextField1.setText(rs.getString("DonorID")); //, rs.getString("DonorName"), rs.getString("Gender"),rs.getInt("Contact"), 
                jTextField2.setText(rs.getString("DonorName"));
                jTextField3.setText(rs.getString("Contact"));
                jTextArea1.setText(rs.getString("Address"));

                jComboBox1.setSelectedItem(rs.getString("BloodGroup"));

                java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse(rs.getString("DOB"));
                jXDatePicker1.setDate(date);
                jTextField5.setText(rs.getString("weight"));
                String gend = rs.getString("Gender");
                if (gend.equals("Male")) {
                    jRadioButton1.setSelected(true);
                } else {
                    jRadioButton2.setSelected(true);
                }

                java.util.Date date2 = new SimpleDateFormat("yyyy-MM-dd").parse(rs.getString("LastDonateDate"));
                jXDatePicker2.setDate(date2);
                jTextField6.setText(rs.getString("Age"));
                jComboBox6.setSelectedItem(rs.getString("Area"));

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnSearchActionPerformed

