    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        try {
            String gender = null;
            String blood = jComboBox1.getSelectedItem().toString();
            String area = jComboBox6.getSelectedItem().toString();

            java.util.Date oDate = jXDatePicker2.getDate();
            DateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");

            String dob = oDateFormat.format(oDate);

            java.util.Date oDate1 = jXDatePicker1.getDate();
            DateFormat oDate1Format = new SimpleDateFormat("yyyy-MM-dd");

            String ldd = oDate1Format.format(oDate1);

            // Date dob=(Date) jXDatePicker2.getDate();
            // String ldd=jXDatePicker1.getDate().toString();
            int contact = Integer.parseInt(jTextField3.getText());
            double weight = Double.parseDouble(jTextField5.getText());
            int age = Integer.parseInt(jTextField6.getText());

            if (jRadioButton1.isSelected()) {
                gender = "Male";
            } else if (jRadioButton2.isSelected()) {
                gender = "Female";
            }

            String query = "INSERT INTO `donor` (`DonorID`, `DonorName`, `Gender`, `Contact`, `Address`, `BloodGroup`, `weight`, `DOB`, `LastDonateDate`, `Area`, `Age`)"
                    + " VALUES ('" + jTextField1.getText() + "', '" + jTextField2.getText() + "', '" + gender + "', '" + contact + "', '" + jTextArea1.getText() + "', '" + blood + "', " + "'" + weight + "', '" + dob + "', '" + ldd + "', '" + area + "', '" + age + "')";

            executeSQlQuery(query, "Inserted");
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(rootPane, "Input format incorrect " + e.getMessage());
          // e. printStackTrace();
        }
    }//GEN-LAST:event_btnAddMouseClicked

